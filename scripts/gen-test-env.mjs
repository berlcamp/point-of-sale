// Writes .env.test.local from the running local Supabase stack so the Next.js
// dev server (webServer in playwright.config.ts) and the auth setup project
// talk to the local backend instead of the remote project in .env.local.
//
// Run automatically by the `test:e2e` npm script; safe to run by hand.

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";

function readStatus() {
  // Use the locally-installed CLI (devDependency), not a global one.
  const out = execSync("npx --no-install supabase status -o env", {
    encoding: "utf8",
  });
  const env = {};
  for (const line of out.split("\n")) {
    const m = line.match(/^(\w+)="(.*)"$/);
    if (m) env[m[1]] = m[2];
  }
  return env;
}

let status;
try {
  status = readStatus();
} catch {
  console.error(
    "Could not read `supabase status`. Start the stack first: `npx supabase start`."
  );
  process.exit(1);
}

if (!status.API_URL || !status.ANON_KEY || !status.SERVICE_ROLE_KEY) {
  console.error("Supabase status is missing API_URL / ANON_KEY / SERVICE_ROLE_KEY.");
  process.exit(1);
}

const contents = `# Generated by scripts/gen-test-env.mjs — do not edit by hand.
# Points the app + Playwright at the local Supabase stack.
NEXT_PUBLIC_SUPABASE_URL=${status.API_URL}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${status.ANON_KEY}
NEXT_PUBLIC_SITE_URL=http://localhost:3000
SUPABASE_SERVICE_ROLE_KEY=${status.SERVICE_ROLE_KEY}
`;

writeFileSync(".env.test.local", contents);
console.log(".env.test.local written for", status.API_URL);
